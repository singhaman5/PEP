let a = 10
let b = 4
// arithmetic operators
console.log(a+b)
console.log(a-b)
console.log(a*b)
console.log(a/b)
console.log(a%b)
console.log(a**b)

// Comparison operators
console.log(a > b)
console.log(a < b)
console.log(a >= b)
console.log(a <= b)
console.log(a == b)
console.log(a != b)

let x = "10"
let y = 10
console.log(x == y) // true // It compares only value
console.log(x === y) // false // It compares both value and type

// Logical operators
console.log(a > b && a > 20) // false
console.log(a > b || a > 20) // true
console.log(!(a > b)) // false